toIntList :: BS.ByteString -> [Int]
toIntList = unfoldr (BS.readInt . BS.dropWhile isSpace)

readIntList :: IO [Int]
readIntList = toIntList <$> BS.getLine

readIntLists :: Int -> IO [[Int]]
readIntLists n = replicateM n readIntList

yn :: Bool -> String
yn True  = "Yes"
yn False = "No"

solve :: Int -> [[Int]] -> [String]
solve n = go S.empty
  where
    go _  []             = []
    go fs ([t, a, b]:qs) = case t of
                             1 -> go (S.insert (a, b) fs) qs
                             2 -> go (S.delete (a, b) fs) qs
                             3 -> yn (S.member (a, b) fs && S.member (b, a) fs):go fs qs

main :: IO ()
main = do
  [n, q] <- readIntList
  qs     <- readIntLists q
  mapM_ putStrLn $ solve n qs
