mmul :: Int -> Int -> Int
mmul x y = (x*y) `mod` modulo

-- https://rosettacode.org/wiki/Modular_inverse
modInv :: Int -> Int -> Maybe Int
modInv a m
  | 1 == g = Just (mkPos i)
  | otherwise = Nothing
  where
    (i, _, g) = gcdExt a m
    mkPos x
      | x < 0 = x + m
      | otherwise = x

-- Extended Euclidean algorithm.
-- Given non-negative a and b, return x, y and g
-- such that ax + by = g, where g = gcd(a,b).
-- Note that x or y may be negative.
gcdExt :: Int -> Int -> (Int, Int, Int)
gcdExt a 0 = (1, 0, a)
gcdExt a b =
  let (q, r) = a `quotRem` b
      (s, t, g) = gcdExt b r
  in (t, s - q * t, g)

solve :: Int -> Int -> Int
solve w h = foldl' mmul 1 (xs ++ ys)
  where
    w' = w - 1
    h' = h - 1
    a  = min w' h'
    xs = [w' + h' - a + 1..w' + h']
    ys = map (fromJust . flip modInv modulo) [1..a]

